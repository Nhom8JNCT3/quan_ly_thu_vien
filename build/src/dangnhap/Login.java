    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Connection connection = (Connection) getJDBCConnection();
        if (thuthuRadioButton.isSelected()){
           
        String sql = "SELECT * FROM BANGQUANTRI WHERE TKNV = '" + txtUid.getText() + "' AND MKNV = '" + txtPwd.getText()+"'";
        try {
            PreparedStatement preparedStatement = connection.prepareCall(sql);
            //preparedStatement.setInt(1, id);
            ResultSet rs = preparedStatement.executeQuery();
            if(rs.next())
            {

                NhanVienFrame m = new NhanVienFrame();
                m.show();
                this.hide();
            }
            else{
                JOptionPane.showMessageDialog(this, "Sai tai khoan hoac mat khau");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        }
        }
        if (docgiaRadioButton.isSelected()){
            String sql = "SELECT * FROM DOCGIA WHERE TENDANGNHAP = '" + txtUid.getText() + "' AND MATKHAU = '" + txtPwd.getText()+"'";
        try {
            PreparedStatement preparedStatement = connection.prepareCall(sql);
            //preparedStatement.setInt(1, id);
            ResultSet rs = preparedStatement.executeQuery();
            if(rs.next())
            {

                DocgiaFrame m = new DocgiaFrame();
                m.show();
                this.hide();
            }
            else{
                JOptionPane.showMessageDialog(this, "Sai tai khoan hoac mat khau");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        }
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

